// create your loops here.
// print 0 through 9
for (let i = 0; i <= 9; i++) {
  console.log(i);
}

// print increments of 2 in 10 loops
for (let i = 0; i <= 9 * 2; i += 2) {
  console.log(i);
}

// count down from 100
for (let i = 100; i >= 0; i -= 1) {
  console.log('Time till explosion: ', i, '!');
}
